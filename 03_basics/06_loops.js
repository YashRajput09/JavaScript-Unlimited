// for loop, run code  block until condition is false
// while loop, keep running the code block as long as the condition is true.
for (let i=0; i<5; i++){
    console.log(i);
}

// while loop, check if conditon is true then  execute the code inside it
let a = 0;
while(a<=5){
    console.log("this is while loop");
    a++
}

// do-while loop, one the code is run and then the condition is checked
let b = 0;
do{
    console.log( "This is Do While Loop" );
    b++;
} while(b<5);